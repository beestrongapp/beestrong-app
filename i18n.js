let lang=(()=>{
  const saved=localStorage.getItem('bs-lang');
  if(saved)return saved;
  const bl=(navigator.language||'en').toLowerCase().slice(0,2);
  const sup=['pl','en','de','es'];
  const detected=sup.includes(bl)?bl:'en';
  localStorage.setItem('bs-lang',detected);
  return detected;
})();
function t(k){return T[lang][k]||k;}
// Inline 4-language picker for ad-hoc strings (cleaner than nested ternary)
function tt(o){return (o&&typeof o==='object')?(o[lang]||o.en||o.pl||''):o;}
// Workout display name — translates known nameKeys, falls back to stored .name
function displayWorkoutName(w){
  if(!w)return'';
  if(w.nameKey)return t(w.nameKey)||w.name||'';
  // Backward compat: detect legacy hard-coded names from earlier saves
  const n=w.name||'';
  if(n==='Quick Workout'||n==='Szybki trening'||n==='Schnelles Training'||n==='Entrenamiento rápido')return t('quickWorkout');
  if(n==='Custom Workout'||n==='Własny trening'||n==='Eigenes Training'||n==='Entrenamiento personalizado')return tt({pl:'Własny trening',en:'Custom Workout',de:'Eigenes Training',es:'Entrenamiento personalizado'});
  return n;
}

function setLang(l){
  lang=l;localStorage.setItem('bs-lang',l);
  applyLang();
  renderDashboard();renderCalendar();renderTemplates();renderWorkout();renderProgress();renderSettings();
}
function applyLang(){
  document.querySelectorAll('[data-key]').forEach(el=>{const k=el.dataset.key;if(T[lang][k])el.textContent=T[lang][k];});
  document.querySelectorAll('.nav-label[data-key]').forEach(el=>el.textContent=t(el.dataset.key));
  const ids={lblNewWorkout:'newWorkout',lblRecentWorkouts:'recentWorkouts',lblNewTemplate:'newTemplate',titleTemplates:'templates',dashTitle:'dashboard',lblQuickWorkout:'quickWorkout',lblQuickWorkoutDesk:'quickWorkout',lblNewWorkoutDesk:'newWorkout',settingsTitle:'settings'};
  Object.entries(ids).forEach(([id,key])=>{const el=document.getElementById(id);if(el)el.textContent=t(key);});
  renderAccountBadge();
  const tl=document.getElementById('themeLabel');if(tl)tl.textContent=isDark?t('lightMode'):t('darkMode');
}

let isDark=localStorage.getItem('bs-theme')!=='light';
function applyTheme(){
  document.body.classList.toggle('light',!isDark);
  const tr=document.getElementById('toggleTrack');const lb=document.getElementById('themeLabel');
  if(tr)tr.classList.toggle('on',!isDark);
  if(lb)lb.textContent=isDark?t('lightMode'):t('darkMode');
  // Switch logo
  const logoImg=document.getElementById('logoImg');
  if(logoImg) logoImg.src=isDark?'./logo.jpg':'./light_logo.png';
  // Switch dashboard background
  const dash=document.getElementById('screen-dashboard');
  if(dash){
    const bg=isDark?`url('./logo.jpg')`:`url('./light_logo.png')`;
    dash.style.setProperty('--dash-bg',bg);
  }

}
function toggleTheme(){isDark=!isDark;localStorage.setItem('bs-theme',isDark?'dark':'light');applyTheme();}

